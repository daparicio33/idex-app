    <h1>Politica de privacidad</h1>
    <p>
        Este documento describe cómo se utiliza la información en nuestra aplicación de Android. Al utilizar esta aplicación, aceptas los términos de esta política de privacidad.

Información
No recopilamos ninguna información personal o no personal sobre ti. La aplicación simplemente brinda la funcionalidad para su uso sin la recopilación de datos adicionales.

Uso de la Información
No utilizamos la información obtenida a través de la aplicación para ningún propósito.

Compartir Información
No compartimos información obtenida a través de la aplicación con terceros.

Seguridad de la Información
No almacenamos información en nuestros servidores y no existe un riesgo de pérdida, robo, uso indebido o acceso no autorizado de información.

Cambios a esta Política de Privacidad
Podemos actualizar esta política de privacidad de vez en cuando. Te notificaremos de cualquier cambio importante mediante un aviso en la aplicación o enviándote un correo electrónico. Al continuar utilizando la aplicación después de dichos cambios, aceptas estar sujeto a la política de privacidad actual.

Contacto
Si tienes preguntas acerca de esta política de privacidad, no dudes en contactarnos en soporte@idexperujapon.edu.pe




